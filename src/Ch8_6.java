public class Ch8_6 {
    public static void main(String[] args) {
        double i = 3.14159;
        double j = 2.71828;
        System.out.println("3.14159的 round()值" + Math.round(i));
        System.out.println("2.71828的 round()值" + Math.round(j));

        double x = -3.14159;
        double y = -2.71828;
        System.out.println("-3.14159的 round()值" + Math.round(x));
        System.out.println("-2.71828的 round()值" + Math.round(y));
    }
}
